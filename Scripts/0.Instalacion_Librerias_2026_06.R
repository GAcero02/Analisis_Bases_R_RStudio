#se instalan los paquetes necesarios para este proceso

#tidyverse es una colección de paquetes de R diseñada para importar, limpiar, 
#transformar, visualizar y analizar datos de manera organizada y consistente
install.packages("tidyverse")

#janitor es un paquete de R que facilita la limpieza y organización de bases de datos 
#ademása estandariza nombres, explora frecuencias y prepara datos para el análisis
install.packages("janitor")

#skimr es un paquete de R que genera resúmenes exploratorios rápidos y detallados 
#de un conjunto de datos, mostrando estadísticas descriptivas según el tipo de variable
install.packages("skimr")

#El paquete rio permite importar y exportar datos en múltiples formatos con 
#funciones simples como import() y export()
install.packages("rio")

#El paquete stringr facilita la manipulación de textos en R, como buscar, 
#reemplazar, extraer, unir y ajustar cadenas de caracteres
install.packages("stringr")

#El paquete ggplot2 permite crear gráficos y visualizaciones de datos de 
#forma flexible y personalizable en R
install.packages("ggplot2")

#El paquete gtsummary permite crear tablas estadísticas descriptivas y de 
#resultados de modelos con un formato claro y listo para presentar.
install.packages("gtsummary")

#El paquete flextable permite crear y personalizar tablas con formato 
#profesional para exportarlas a Word, PowerPoint y otros documentos
install.packages("flextable")

#El paquete officer permite crear, leer y modificar documentos de Word y 
#PowerPoint directamente desde R.
install.packages("officer")

